# Tipos Primitivos em Python

# 1. Inteiros (int)
numero_inteiro = 10
print("int:", numero_inteiro, type(numero_inteiro))

# 2. Ponto flutuante (float)
numero_decimal = 3.14
print("float:", numero_decimal, type(numero_decimal))

# 3. Booleano (bool)
valor_verdadeiro = True
valor_falso = False
print("bool (True):", valor_verdadeiro, type(valor_verdadeiro))
print("bool (False):", valor_falso, type(valor_falso))

# 4. Texto (str)
texto = "Olá, Python!"
print("str:", texto, type(texto))

# 5. None (tipo nulo - NoneType)
nulo = None
print("None:", nulo, type(nulo))

# Extras: tipos de coleção que não são primitivos, mas são muito usados

# Lista (list)
lista_exemplo = [1, 2, 3]
print("list:", lista_exemplo, type(lista_exemplo))

# Tupla (tuple)
tupla_exemplo = (1, 2, 3)
print("tuple:", tupla_exemplo, type(tupla_exemplo))

# Conjunto (set)
conjunto_exemplo = {1, 2, 3}
print("set:", conjunto_exemplo, type(conjunto_exemplo))

# Dicionário (dict)
dicionario_exemplo = {"nome": "Matheus", "idade": 33}
print("dict:", dicionario_exemplo, type(dicionario_exemplo))
