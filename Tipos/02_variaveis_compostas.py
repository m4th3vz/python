# Lista: mutável e ordenada
frutas = ['maçã', 'banana', 'laranja']
print("Lista de frutas:", frutas)

# Tupla: imutável e ordenada
coordenadas = (10, 20)
print("Tupla de coordenadas:", coordenadas)

# Dicionário: chave-valor
pessoa = {'nome': 'Matheus', 'idade': 33}
print("Dicionário de pessoa:", pessoa)

# Conjunto: elementos únicos e sem ordem
numeros = {1, 2, 3, 2}
print("Conjunto de números:", numeros)
