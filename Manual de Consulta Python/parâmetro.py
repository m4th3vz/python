# Definindo uma função com dois parâmetros: nome e idade
def apresentar(nome, idade): # nome e idade são os parâmetros
    print(f"Olá, meu nome é {nome} e tenho {idade} anos.")

# Chamando a função e passando os argumentos
apresentar("Matheus", 33)

# ➤ Parâmetro é a variável usada na definição da função para receber um valor.
# ➤ Neste exemplo, 'nome' e 'idade' são os parâmetros da função apresentar().
# ➤ Quando chamamos a função, passamos os argumentos que serão atribuídos aos parâmetros.
