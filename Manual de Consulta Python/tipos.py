# Tipos primitivos em Python

# Inteiro (int)
idade = 33
print(type(idade))  # <class 'int'>

# Número decimal (float)
altura = 1.75
print(type(altura))  # <class 'float'>

# Texto (str)
nome = "Matheus"
print(type(nome))  # <class 'str'>

# Booleano (bool)
ativo = True
print(type(ativo))  # <class 'bool'>

# Lista (list)
frutas = ["maçã", "banana", "laranja"]
print(type(frutas))  # <class 'list'>

# Tupla (tuple)
coordenadas = (23.5, 45.6)
print(type(coordenadas))  # <class 'tuple'>

# Conjunto (set)
letras = {"a", "b", "c"}
print(type(letras))  # <class 'set'>

# Dicionário (dict)
pessoa = {"nome": "Matheus", "idade": 33}
print(type(pessoa))  # <class 'dict'>

# ➤ Python possui tipos integrados (int, float, str, bool, list, tuple, set, dict).
# ➤ Um tipo (ou type) representa a classificação de um valor ou objeto, indicando que tipo de dado ele armazena e quais operações são permitidas sobre ele.
# ➤ Podemos verificar o tipo de uma variável usando a função type().
