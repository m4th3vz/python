# Lista (list) em Python funcionando como um array
numeros = [10, 20, 30, 40, 50]

# Acessando elementos pelo índice
print(numeros[0])  # Imprime: 10
print(numeros[-1])  # Imprime: 50 (último elemento)

# Adicionando um novo elemento
numeros.append(60)

# Percorrendo todos os elementos
for numero in numeros:
    print(numero)

# ➤ Em Python, usamos listas como arrays.
# São estruturas que armazenam vários valores em uma única variável.
# Os elementos são acessados por índice (começando do 0).
