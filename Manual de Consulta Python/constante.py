# Convenção para constante em Python: nome em letras maiúsculas
PI = 3.14159
TAXA_DE_JUROS = 0.05

print(f"Valor de PI: {PI}")
print(f"Taxa de juros: {TAXA_DE_JUROS}")

# Embora possamos alterar o valor, a convenção indica que não devemos fazer isso
# Exemplo:
# PI = 3  # Não recomendado, mas o Python permite

# ➤ Constante é um valor que não deve mudar durante a execução do programa.
# ➤ Em Python, usamos a convenção de nomes em maiúsculas para indicar constantes, mas o interpretador não impede alterações.
