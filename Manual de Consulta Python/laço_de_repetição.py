# Laço for: percorre uma sequência (lista, range, etc.)
print("Laço for:")
for i in range(1, 6):  # vai de 1 até 5
    print(i)

# Laço while: executa enquanto a condição for verdadeira
print("\nLaço while:")
contador = 1
while contador <= 5:
    print(contador)
    contador += 1  # incrementa o contador para evitar loop infinito

# ➤ Laços de repetição servem para executar um bloco de código várias vezes.
# ➤ For é ideal quando sabemos o número de repetições.
# ➤ While é usado quando repetimos até uma condição mudar.
