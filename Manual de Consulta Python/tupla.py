# Criando uma tupla com alguns dados
dados = ("Matheus", 33, "Barueri")

# Acessando elementos por índice
print(dados[0])  # Imprime: Matheus
print(dados[1])  # Imprime: 33

# Percorrendo a tupla
for item in dados:
    print(item)

# ➤ Tupla (tuple) é uma coleção ordenada e imutável de elementos.
# ➤ Usa parênteses e é semelhante à lista, mas não pode ser modificada após a criação.
# ➤ Ideal para representar dados fixos ou que não devem ser alterados.
