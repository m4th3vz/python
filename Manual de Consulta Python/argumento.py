# Função que recebe dois argumentos: nome e idade
def apresentar(nome, idade):
    print(f"Olá, meu nome é {nome} e tenho {idade} anos.")

# Chamando a função e passando os argumentos
apresentar("Matheus", 33)

# ➤ Argumento é o valor que passamos para uma função.
# Neste exemplo, "Matheus" e 32 são os argumentos passados
# para os parâmetros 'nome' e 'idade' da função apresentar.
