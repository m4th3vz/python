# Criando variáveis
nome = "Matheus"
idade = 33
altura = 1.75
ativo = True

# Usando as variáveis
print(f"Nome: {nome}")
print(f"Idade: {idade}")
print(f"Altura: {altura}")
print(f"Ativo: {ativo}")

# ➤ Variável é um nome que armazena um valor na memória.
# ➤ O valor pode ser de qualquer tipo (texto, número, booleano, lista etc).
# ➤ Em Python, você não precisa declarar o tipo da variável — ele é determinado automaticamente.
