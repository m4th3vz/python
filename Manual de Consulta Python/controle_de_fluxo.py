# Controle de fluxo com if, for e while

idade = 33

# Condicional if-else
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")

# Loop for para contar de 1 a 5
for i in range(1, 6):
    print(f"Contagem com 'for': {i}")

# Loop while para contar de 5 a 1
contador = 5
while contador > 0:
    print(f"Contagem com 'while': {contador}")
    contador -= 1

# ➤ Controle de fluxo determina o caminho que o programa vai seguir.
# ➤ if/else executa blocos condicionais.
# ➤ for e while são estruturas de repetição (loops).
