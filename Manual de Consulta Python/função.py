# Definindo uma função que recebe dois números e retorna a soma
def somar(a, b):
    return a + b

# Chamando a função e armazenando o resultado
resultado = somar(10, 23)

print(f"A soma é: {resultado}")

# ➤ Função é um bloco de código reutilizável que realiza uma tarefa específica.
# ➤ Pode receber argumentos (entradas) e retornar um valor (saída).
