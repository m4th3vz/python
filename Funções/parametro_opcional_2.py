def soma(a=0, b=0, c=0):
    return a + b + c

# Exemplos de uso:
print(soma())           # Saída: 0 (nenhum argumento passado)
print(soma(5))          # Saída: 5 (apenas 'a' recebe valor)
print(soma(3, 2))       # Saída: 5 (a=3, b=2, c usa valor padrão 0)
print(soma(1, 2, 3))    # Saída: 6 (todos os argumentos passados)
