def soma(a=0, b=0, c=0):
    """
    Soma até três números e retorna o resultado.

    Parâmetros:
    a (int ou float): Primeiro número. Padrão é 0.
    b (int ou float): Segundo número. Padrão é 0.
    c (int ou float): Terceiro número. Padrão é 0.

    Retorna:
    int ou float: A soma de a, b e c.
    """
    return a + b + c

help(soma)
