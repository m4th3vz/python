# Contém funções matemáticas, como trigonometria, exponenciais, logaritmos e operações de precisão.
import math

# Raiz quadrada
print("Raiz quadrada de 25:", math.sqrt(25))

# Potência
print("2 elevado a 3:", math.pow(2, 3))  # retorna float

# Arredondamento pra baixo e pra cima
print("Arredonda pra baixo 3.9:", math.floor(3.9))
print("Arredonda pra cima 3.1:", math.ceil(3.1))

# Valor absoluto
print("Valor absoluto de -10:", math.fabs(-10))

# Pi e seno
print("Pi:", math.pi)
print("Seno de 90 graus:", math.sin(math.radians(90)))
