# Retorna uma nova string com todos os caracteres alfabéticos convertidos para minúsculas.
# Caracteres que não são letras permanecem inalterados.

print("MATHEUS".lower())        # "matheus"
print("Python 3.10".lower())    # "python 3.10"
print("Olá, MUNDO!".lower())    # "olá, mundo!"
print("1234".lower())           # "1234"
print("".lower())               # ""
