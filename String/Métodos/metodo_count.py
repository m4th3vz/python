# Retorna o número de vezes que um valor aparece na string.
# Pode receber índices opcionais para contar dentro de um intervalo.

print("banana".count("na"))           # 2
print("matheus".count("a"))           # 1
print("abcabcabc".count("abc"))       # 3
print("aaaaa".count("aa"))            # 2 (as contagens não se sobrepõem)
print("python".count("z"))            # 0
