# Retorna True se todos os caracteres alfabéticos da string estiverem em maiúsculas.
# Caracteres que não são letras são ignorados. Retorna False se não houver letras.

print("MATHEUS".isupper())     # True  
print("Matheus".isupper())     # False  
print("123ABC!".isupper())     # True  
print("123".isupper())         # False  
print("".isupper())            # False  
