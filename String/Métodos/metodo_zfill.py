# Retorna uma nova string preenchida com zeros à esquerda até atingir o comprimento desejado.
# Útil para formatação de números como códigos ou datas.

print("42".zfill(5))         # "00042"
print("123".zfill(6))        # "000123"
print("-89".zfill(4))        # "-089"
print("matheus".zfill(10))   # "000matheus"
print("".zfill(3))           # "000"
