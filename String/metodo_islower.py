# Retorna True se todos os caracteres alfabéticos da string estiverem em minúsculas.
# Caracteres que não são letras são ignorados. Retorna False se não houver letras.

print("matheus".islower())     # True
print("Matheus".islower())     # False
print("matheus123".islower())  # True
print("123".islower())         # False
print("".islower())            # False
