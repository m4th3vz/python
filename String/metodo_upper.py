# Retorna uma nova string com todos os caracteres alfabéticos convertidos para maiúsculas.
# Caracteres que não são letras permanecem inalterados.

print("matheus".upper())        # "MATHEUS"
print("Python 3.10".upper())    # "PYTHON 3.10"
print("Olá, mundo!".upper())    # "OLÁ, MUNDO!"
print("1234".upper())           # "1234"
print("".upper())               # ""
