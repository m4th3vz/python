# Retorna uma nova string com as letras minúsculas convertidas para maiúsculas e as maiúsculas convertidas para minúsculas.

print("Matheus".swapcase())        # "mATHEUS"
print("PyThOn 3.10".swapcase())    # "pYtHoN 3.10"
print("Olá, MUNDO!".swapcase())    # "oLÁ, mundo!"
print("1234".swapcase())           # "1234"
print("".swapcase())               # ""
