produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2)      # 30   → soma: 20 + 10
print(produto_1 - produto_2)      # 10   → subtração: 20 - 10
print(produto_1 / produto_2)      # 2.0  → divisão: 20 / 10
print(produto_1 // produto_2)     # 2    → divisão inteira: 20 // 10
print(produto_1 * produto_2)      # 200  → multiplicação: 20 * 10
print(produto_1 % produto_2)      # 0    → módulo (resto): 20 % 10
print(produto_1 ** produto_2)     # 10240000000000 → exponenciação: 20 ** 10

x = (10 + 5) * 4
print(x)                          # 60   → (15) * 4

y = (10 / 2) + 25 * ((2 - 2) ** 2)
print(y)                          # 5.0 → 5.0 + 25 * (0 ** 2) = 5.0 + 0 = 5.0
